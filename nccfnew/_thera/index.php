			<h2 class="section-header">You Are Welcome</h2>
			<img src="_css/_images/chairman.jpg" alt="" class="chairman-img">
			<p>It is with great excitement and joy I welcome every Corper to Nigeria Christian Corpers’ Fellowship (NCCF) Cross River State Chapter. Your being in this state is a sign that God answers prayers, we have been praying that the Lord bring you and here you are in Cross River state. For this reason, I praise the name of God forever.</p>
			<p>I like you to know God was so deliberate to have brought you to Cross River State, beyond your connections and efforts to be here, God actually permitted it to work out for a reason and par adventure you think Cross River is not where you would have loved to serve, I like you to know that God has a plan for you here. So please settle down, be sensitive and cooperate with God to bring to pass what He intends to do in you, for you and through you.</p>
			<p>At the beginning of this tenure, God assured us that His counsel for us as a fellowship in this season is to make His kingdom come in our lives and by extension in the state. Hence, the theme for 2017/2018 tenure, Thy Kingdom Come (Matthew 6:10).</p>
			<p>The eternal counsel of God has always been that man reflect, practice and demonstrate the very nature and attribute of God, to see to it that man is launched into experiential knowledge and realities of the Kingdom of God here on earth.</p>
			<p>When we talk of Thy Kingdom Come, what we are saying is that we want to replicate the character of our King (God) and reproduce His will here on earth. 1 John 4:17 says, “As God is in Heaven, so are we on earth”. Enough of living an ordinary life, the season of the supernatural and extraordinary is here.</p>
			<p>I enjoin you that wherever you find yourself serving across the local government of the state, have it in mind that God has made you a king and a priest unto Himself, therefore you shall reign on the earth (Revelation 5:10).</p>
			<p>Please be an active member of NCCF in any of the 12 zones we have in the state and that the Lord will bless you richly.
			The Lord keep, preserve you and cause His will to come through in your life throughout your stay in Cross River (Amen). <br>
			<strong>I love and celebrate you greatly.</strong></p>

			</p>
