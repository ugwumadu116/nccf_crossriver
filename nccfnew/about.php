			<h3 class="subSection-header">Who We Are</h3>
			<p>Nigeria Christian Corpers' Fellowship (NCCF), Cross River state Chapter was established after the National Youth Service Corps, and the State Secretariat is located in Calabar, the state's capital. </p>
			<p>In the administration of NCCF in Cross River, there are 13 Zones: Akamkpa Zone, Akpabuyo Zone, Bakassi Zone , Bekwara Zone , Boki East, Boki West Zone, Calabar Zone, Ikom Zone, Obubra Zone, Obudu Zone, Odukpani Zone, Ogoja Zone, Yakurr Zone.</p>
			<p>Although the number of Zonal Executive Committee members varies by Zone, the Central Executive Committee, domiciled at the State Secretariat, is made up of 13 members: the President, General Secretary, Assistant General Secretary, Treasurer, Financial Secretary, Transport and Organizing Secretary, Bible Study Secretary, Prayer Secretary, Evangelism Secretary, Publicity Secretary, Drama Unit Secretary, Music Director, and Sisters Coordinator.</p>
			<p>In every zone, there is a Family House which serves as a lodge for members of NCCF.  Just like every other family house, this branch is bound by ethics. The ethics are not just rules to be followed without asking questions but they are the cords of love that bind us together.</p>
